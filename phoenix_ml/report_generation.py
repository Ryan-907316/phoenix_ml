def format_duration(total_seconds: float) -> str:
    """Human-readable duration string, e.g. '18m 26.0s' or '1h 2m 3.0s'."""
    total = max(total_seconds, 0.0)
    if total >= 86400:
        d, r = divmod(total, 86400); h, r = divmod(r, 3600); m, s = divmod(r, 60)
        return f"{int(d)}d {int(h)}h {int(m)}m {s:.1f}s"
    elif total >= 3600:
        h, r = divmod(total, 3600); m, s = divmod(r, 60)
        return f"{int(h)}h {int(m)}m {s:.1f}s"
    elif total >= 60:
        m, s = divmod(total, 60)
        return f"{int(m)}m {s:.1f}s"
    else:
        return f"{total:.1f}s"


