def add_interpretability_metrics_table(elements, styles, metrics_df, stage_label=""):
    """Per-(model, target) comparable summary table — Morris/Sobol top feature per
    method, their rank agreement, and monotonicity sanity-check mismatches.
    See interpretability.compute_interpretability_metrics().
    """
    if metrics_df is None or metrics_df.empty:
        return
    heading = f"Interpretability Metrics Summary ({stage_label})" if stage_label else "Interpretability Metrics Summary"
    elements.append(Paragraph(heading, styles["CustomHeading"]))
    elements.append(Paragraph(
        "The top feature and their corresponding metrics are summarised below, with one row "
        "per model and target. \"Rank Agreement\" is the Spearman correlation between the "
        "Morris and Sobol feature rankings (only when both are enabled). This table is also "
        "included in the Excel results file.",
        styles["CustomBody"],
    ))
    elements.append(Spacer(1, 6))

    cols = [c for c in metrics_df.columns if metrics_df[c].notna().any()]
    df = metrics_df[cols].copy()
    for c in df.columns:
        if pd.api.types.is_float_dtype(df[c]):
            df[c] = df[c].map(lambda v: "" if pd.isna(v) else f"{v:.3f}")
        else:
            df[c] = df[c].fillna("")

    # "Top Feature" columns are "FeatureName (value)" — that's the widest content in
    # the table by far, and with every method enabled the table can run very wide.
    # Breaking the value onto its own line (via a Paragraph, since plain Table cells
    # can't wrap on a tag) roughly halves those columns' width at the cost of a
    # taller row, which is the more compact trade-off when many rows are present.
    top_feature_style = ParagraphStyle(name="TopFeatureCell", fontSize=6, leading=7)
    top_feature_cols = {"Morris Top Feature", "Sobol Top Feature", "FAST Top Feature"}
    for c in df.columns:
        if c in top_feature_cols:
            df[c] = df[c].map(
                lambda v: Paragraph(v.replace(" (", "<br/>(", 1), top_feature_style) if v else ""
            )

    data = [list(df.columns)] + df.values.tolist()
    tbl = Table(data, repeatRows=1)
    style_cmds = [
        ('BACKGROUND', (0, 0), (-1, 0), colors.HexColor("#333333")),
        ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),
        ('FONTSIZE', (0, 0), (-1, -1), 6),
        ('GRID', (0, 0), (-1, -1), 0.5, colors.grey),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('TOPPADDING', (0, 0), (-1, -1), 2),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 2),
    ]
    for i in range(1, len(data)):
        if i % 2 == 0:
            style_cmds.append(('BACKGROUND', (0, i), (-1, i), colors.HexColor("#f7f7f7")))
    tbl.setStyle(TableStyle(style_cmds))
    elements.append(tbl)
    elements.append(Spacer(1, 12))


