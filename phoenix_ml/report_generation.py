def _split_method_phrase(split_method, test_prop):
    """Plain-language description of how the test set was chosen, rather than the
    internal split_method keyword on its own (e.g. 'random', which reads as jargon
    out of context)."""
    pct = f"{test_prop:.0%}" if isinstance(test_prop, (int, float)) else "the requested proportion of"
    method = (split_method or "").lower()
    if method == "random":
        return "a random split"
    if method == "first":
        return f"the first {pct} of rows as the test set"
    if method == "last":
        return f"the last {pct} of rows as the test set"
    return f"the '{split_method}' method" if split_method else "an unspecified split method"


def _join_and(items):
    items = [str(i) for i in items]
    if not items:
        return ""
    if len(items) == 1:
        return items[0]
    if len(items) == 2:
        return f"{items[0]} and {items[1]}"
    return ", ".join(items[:-1]) + f" and {items[-1]}"


def _exec_table(rows, col_widths, header=True):
    style_cmds = [
        ('FONTSIZE', (0, 0), (-1, -1), 7.5),
        ('GRID', (0, 0), (-1, -1), 0.25, colors.grey),
        ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
        ('LEFTPADDING', (0, 0), (-1, -1), 4),
        ('RIGHTPADDING', (0, 0), (-1, -1), 4),
        ('TOPPADDING', (0, 0), (-1, -1), 2),
        ('BOTTOMPADDING', (0, 0), (-1, -1), 2),
    ]
    if header:
        style_cmds += [
            ('BACKGROUND', (0, 0), (-1, 0), _HDR_BG),
            ('TEXTCOLOR', (0, 0), (-1, 0), _HDR_FG),
            ('FONTNAME', (0, 0), (-1, 0), 'Helvetica-Bold'),
        ]
    tbl = Table(rows, colWidths=col_widths, hAlign="LEFT")
    tbl.setStyle(TableStyle(style_cmds))
    return tbl


def _exec_dataset_group(styles, preprocessing_results, dataset_path, perl_config, cleaning_summary,
                        random_seed=None):
    meta = (preprocessing_results or {}).get("meta")
    if not meta:
        return []
    name = os.path.basename(str(meta.get("dataset_path", dataset_path) or "").replace("\\", "/"))
    targets = meta.get("targets", [])
    target_phrase = _join_and(targets) or "not recorded"
    were_was = "were" if len(targets) != 1 else "was"

    sentences = [
        f"The dataset used for this run was {name}, containing "
        f"{meta.get('n_rows', '?')} rows and {meta.get('n_cols', '?')} columns "
        f"({meta.get('n_features', '?')} features after preprocessing). "
        f"The target variable(s) modelled {were_was} {target_phrase}.",
        f"Data were split into {meta.get('train_count', '?')} training rows "
        f"({meta.get('train_prop', 0):.1%}) and {meta.get('test_count', '?')} test rows "
        f"({meta.get('test_prop', 0):.1%}), using "
        f"{_split_method_phrase(meta.get('split_method'), meta.get('test_prop'))}, "
        f"with features scaled using {meta.get('scaler_name', 'StandardScaler')}.",
        f"The random seed was {random_seed if random_seed is not None else 0}.",
    ]

    if cleaning_summary:
        rows_before = cleaning_summary.get("rows_before")
        rows_after = cleaning_summary.get("rows_after")
        if rows_before is not None and rows_after is not None:
            removed = rows_before - rows_after
            pct = (removed / rows_before * 100) if rows_before else 0.0
            sentences.append(
                f"Before this run, the dataset was cleaned: {rows_before} rows were reduced to "
                f"{rows_after} ({removed} removed, {pct:.1f}%). See the Dataset Cleaning log for "
                "the specific actions applied."
            )
        else:
            sentences.append("The dataset was cleaned before this run; see the Dataset Cleaning log for detail.")

    if perl_config:
        mode_label = "Expression Mode" if perl_config.get("mode") == "expression" else "Script Mode"
        sentences.append(
            f"This run used Physics-Enhanced Residual Learning (PERL) in {mode_label}, combining a "
            "physics-based estimate with a residual machine learning model for each target."
        )

    return [
        Paragraph("Dataset", _EXEC_LABEL_STYLE),
        Paragraph(" ".join(sentences), styles["CustomBody"]),
    ]


def _exec_modelling_group(styles, selected_model_names, results_df,
                          hpo_metric, best_models_per_target, perl_results,
                          uq_before, uq_after, uq_settings, postprocessing_results,
                          preprocessing_results):
    if not selected_model_names:
        return []
    out = [Paragraph("Modelling", _EXEC_LABEL_STYLE)]

    n_models = len(selected_model_names)
    model_word = "model" if n_models == 1 else "models"
    lead = f"{n_models} candidate {model_word} ({_join_and(selected_model_names)}) were trained and compared."

    best = best_models_per_target if isinstance(best_models_per_target, dict) else None
    any_optimised = bool(best) and any(row.get("hpo_method", "Default") != "Default" for row in best.values())
    if any_optimised:
        lead += " The best-performing model and hyperparameters found for each target, after optimisation, were:"
    elif best:
        lead += " The best-performing model for each target, using default hyperparameters, was:"

    out.append(Paragraph(lead, styles["CustomBody"]))

    if best:
        metric_display = _fmt_metric(hpo_metric)
        rows = [["Target Variable", "Best Model", metric_display, "Selected via"]]
        for target, row in best.items():
            score = row.get(hpo_metric)
            score_str = f"{score:.4f}" if isinstance(score, (int, float)) and score == score else "N/A"
            method = row.get("hpo_method", "Default")
            via = "default hyperparameters" if method == "Default" else method
            rows.append([str(target), str(row.get("model_name", "?")), score_str, via])
        out.append(Spacer(1, 3))
        out.append(_exec_table(rows, [35*mm, 40*mm, 22*mm, 60*mm]))
    elif results_df is not None and not results_df.empty and "Target Variable" in results_df.columns:
        metric_col = next((c for c in [hpo_metric, "ADJUSTED R^2", "R^2", "RMSE"]
                           if c in results_df.columns), None)
        if metric_col:
            lower_is_best = metric_col.upper() in {"MSE", "RMSE", "NRMSE", "MAPE", "MAE"}
            rows = [["Target Variable", "Best Model", _fmt_metric(metric_col)]]
            for target in results_df["Target Variable"].unique():
                sub = results_df[results_df["Target Variable"] == target]
                vals = pd.to_numeric(sub[metric_col], errors="coerce")
                if vals.dropna().empty:
                    continue
                best_idx = vals.idxmin() if lower_is_best else vals.idxmax()
                rows.append([str(target), str(sub.loc[best_idx, "Model"]), f"{vals[best_idx]:.4f}"])
            if len(rows) > 1:
                out.append(Spacer(1, 3))
                out.append(_exec_table(rows, [45*mm, 55*mm, 30*mm]))

    # Physics-enhanced (PERL) performance, folded in as another modelling outcome rather
    # than a standalone section, so it is weighted the same as any other result here.
    if perl_results:
        targets = list(perl_results.keys())
        if len(targets) == 1:
            v = perl_results[targets[0]]
            rp, rl = v.get("rmse_physics"), v.get("rmse_perl")
            if rp is not None and rl is not None and rp == rp and rl == rl and rp:
                imp = f"{(rp - rl) / rp * 100:+.1f}%"
                out.append(Paragraph(
                    f"Combining the physics estimate with a residual model (PERL) changed the RMSE "
                    f"from {rp:.4f} (physics-only) to {rl:.4f}, an improvement of {imp}.",
                    styles["CustomBody"],
                ))
        else:
            rows = [["Target Variable", "Physics-only RMSE", "PERL RMSE", "Improvement"]]
            for t in targets:
                v = perl_results[t]
                rp, rl = v.get("rmse_physics"), v.get("rmse_perl")
                if rp is None or rl is None or rp != rp or rl != rl or not rp:
                    continue
                imp = f"{(rp - rl) / rp * 100:+.1f}%"
                rows.append([t, f"{rp:.4f}", f"{rl:.4f}", imp])
            if len(rows) > 1:
                out.append(Paragraph(
                    "Combining the physics estimate with a residual model (PERL) changed the RMSE "
                    "for each target as follows:", styles["CustomBody"],
                ))
                out.append(_exec_table(rows, [55*mm, 35*mm, 30*mm, 30*mm]))

    # Uncertainty quantification and cross-validation: one sentence each, as supporting
    # evidence for how much the modelling result above can be trusted.
    if uq_before is not None or uq_after is not None:
        stages = [s for s, present in (("before", uq_before is not None),
                                       ("after", uq_after is not None)) if present]
        stage_phrase = (_join_and(stages) + " hyperparameter optimisation") if len(stages) > 1 \
            else f"{stages[0]} hyperparameter optimisation"
        method = _UQ_METHOD_NAMES.get((uq_settings or {}).get("uq_method"),
                                      (uq_settings or {}).get("uq_method", "an uncertainty"))
        ci = (uq_settings or {}).get("confidence_interval", "N/A")
        out.append(Paragraph(
            f"Prediction uncertainty was quantified using {method} at a {ci}% confidence level, "
            f"computed {stage_phrase}; see the Uncertainty Quantification section for per-model "
            "interval widths and coverage.",
            styles["CustomBody"],
        ))

    cv_df = (postprocessing_results or {}).get("cv_summary_df")
    if cv_df is not None and not cv_df.empty:
        scoring_metric = postprocessing_results.get("scoring_metric", "R²")
        if len(cv_df) == 1:
            r = cv_df.iloc[0]
            out.append(Paragraph(
                f"Cross-validation ({r['CV Method']}) gave a mean "
                f"{_fmt_metric_long(scoring_metric)} of {float(r['Mean Score']):.4f} "
                f"± {float(r['Std Deviation']):.4f}.",
                styles["CustomBody"],
            ))
        else:
            out.append(Paragraph(
                f"Cross-validation used {_fmt_metric_long(scoring_metric)} as the evaluation metric:",
                styles["CustomBody"],
            ))
            rows = [["Target Variable", "CV Method", "Mean Score", "Standard Deviation"]]
            for _, r in cv_df.iterrows():
                rows.append([str(r["Target Variable"]), str(r["CV Method"]),
                            f"{float(r['Mean Score']):.4f}", f"{float(r['Std Deviation']):.4f}"])
            out.append(Spacer(1, 3))
            out.append(_exec_table(rows, [45*mm, 40*mm, 30*mm, 30*mm]))

    feat_block = _exec_top_features(postprocessing_results, preprocessing_results)
    if feat_block:
        out.append(Paragraph(feat_block, styles["CustomBody"]))

    return out


def _exec_top_features(postprocessing_results, preprocessing_results):
    """Most influential feature per target. Prefers LOFO importance when available: it
    retrains with each feature entirely removed rather than shuffled, so a correlated
    substitute can't quietly absorb the dropped feature's signal, making it a more
    reliable signal under multicollinearity than permutation importance. Falls back to
    permutation importance (computed on the actual fitted model), then to the
    distance-correlation relevance ranking from preprocessing (association with the
    target, not tied to a specific model) if postprocessing did not run."""
    lofo = (postprocessing_results or {}).get("lofo_importance")
    perm = (postprocessing_results or {}).get("permutation_importance")
    if lofo:
        bits = []
        disagreements = []
        for target, res in lofo.items():
            importances = res.get("importances")
            names = res.get("feature_names")
            if importances is None or names is None or len(importances) == 0:
                continue
            top_idx = max(range(len(importances)), key=lambda i: importances[i])
            lofo_top = names[top_idx]
            bits.append(f"{lofo_top} for {target} (LOFO importance {importances[top_idx]:.4f})")

            # Cross-check against permutation importance for the same target: LOFO can
            # still be fooled by a correlated substitute remaining in the reduced set
            # (see the caveat text in the Permutation/LOFO Importance report section),
            # so flag it here rather than presenting the LOFO answer as unqualified fact.
            perm_res = (perm or {}).get(target)
            if perm_res:
                perm_means = perm_res.get("importances_mean")
                perm_names = perm_res.get("feature_names")
                if perm_means is not None and perm_names is not None and len(perm_means) > 0:
                    perm_top = perm_names[max(range(len(perm_means)), key=lambda i: perm_means[i])]
                    if perm_top != lofo_top:
                        disagreements.append(f"{target} (permutation instead ranks {perm_top} highest)")

        if bits:
            summary = f"By LOFO importance, the most influential feature was {_join_and(bits)}."
            if disagreements:
                summary += (
                    f" Note: this disagrees with permutation importance for {_join_and(disagreements)}. "
                    f"See the Interpretability and Permutation/LOFO Importance sections for a fuller "
                    f"discussion of this disagreement under multicollinearity."
                )
            return summary

    perm = (postprocessing_results or {}).get("permutation_importance")
    if perm:
        bits = []
        for target, res in perm.items():
            means = res.get("importances_mean")
            names = res.get("feature_names")
            if means is None or names is None or len(means) == 0:
                continue
            top_idx = max(range(len(means)), key=lambda i: means[i])
            bits.append(f"{names[top_idx]} for {target} (permutation importance {means[top_idx]:.4f})")
        if bits:
            return f"By permutation importance, the most influential feature was {_join_and(bits)}."

    feat_sel = (preprocessing_results or {}).get("feature_selection")
    summary_df = (feat_sel or {}).get("summary_df")
    if summary_df is not None and not summary_df.empty and "Max dcor (target)" in summary_df.columns:
        top_row = summary_df.loc[summary_df["Max dcor (target)"].idxmax()]
        return (
            f"By distance correlation with the target, the most relevant feature was "
            f"{top_row['Feature']} (max dcor {top_row['Max dcor (target)']:.4f})."
        )
    return None


def _exec_diagnostics_group(styles, preprocessing_results, postprocessing_results):
    multicoll = (preprocessing_results or {}).get("multicollinearity")
    feat_sel = (preprocessing_results or {}).get("feature_selection")
    ext_df = (postprocessing_results or {}).get("extended_diagnostics_df")
    if not multicoll and not feat_sel and (ext_df is None or ext_df.empty):
        return []

    sentences = []
    if multicoll:
        cond = multicoll.get("condition_number")
        vif_df = multicoll.get("vif_df")
        if cond is not None and vif_df is not None and not vif_df.empty:
            n_severe = int((vif_df["VIF"] > 10).sum())
            n_review = int(((vif_df["VIF"] > 5) & (vif_df["VIF"] <= 10)).sum())
            sentences.append(
                f"Multicollinearity diagnostics gave a condition number of {cond:.2f} for the "
                f"scaled features, with {n_severe} of {len(vif_df)} {_plural(len(vif_df), 'feature')} "
                f"showing a VIF above 10 and {n_review} between 5 and 10."
            )
        elif cond is not None:
            sentences.append(f"Multicollinearity diagnostics gave a condition number of {cond:.2f} "
                             "for the scaled features.")
    if feat_sel:
        summary_df = feat_sel.get("summary_df")
        if summary_df is not None and not summary_df.empty:
            status = summary_df.get("Status", pd.Series(dtype=str)).astype(str)
            n_redundant = int(status.str.contains("Redundant").sum())
            n_noise = int(status.str.contains("Noise").sum())
            sentences.append(
                f"Feature-selection screening flagged {n_redundant} of {len(summary_df)} "
                f"{_plural(len(summary_df), 'feature')} as redundant with another feature and "
                f"{n_noise} as indistinguishable from noise."
            )
    if ext_df is not None and not ext_df.empty:
        bp = pd.to_numeric(ext_df.get("BP p-value"), errors="coerce")
        wh = pd.to_numeric(ext_df.get("White p-value"), errors="coerce")
        dw = pd.to_numeric(ext_df.get("Durbin-Watson"), errors="coerce")
        n = len(ext_df)
        n_hetero = int(((bp < 0.05) | (wh < 0.05)).sum())
        n_autocorr = int(((dw < 1.5) | (dw > 2.5)).sum())
        sentences.append(
            f"Residual diagnostics on the best {_plural(n, 'model')} flagged {n_hetero} of {n} "
            f"target-model {_plural(n, 'combination')} for heteroscedasticity (Breusch-Pagan or "
            f"White p below 0.05) and {n_autocorr} for autocorrelation (Durbin-Watson outside 1.5 to 2.5)."
        )
    if not sentences:
        return []
    return [
        Paragraph("Diagnostics", _EXEC_LABEL_STYLE),
        Paragraph(" ".join(sentences), styles["CustomBody"]),
    ]


def _exec_computation_group(styles, step_timings):
    if not step_timings:
        return []
    timings = [(n, t) for n, t in step_timings if t > 0]
    if not timings:
        return []
    total = sum(t for _, t in timings)
    top_name, top_val = max(timings, key=lambda x: x[1])
    top_pct = (top_val / total * 100) if total else 0.0
    return [
        Paragraph("Computation", _EXEC_LABEL_STYLE),
        Paragraph(
            f"The full workflow took {format_duration(total)} to run, of which {top_name} "
            f"accounted for the largest share at {top_pct:.1f}% ({format_duration(top_val)}).",
            styles["CustomBody"],
        ),
    ]


def add_executive_summary_section(
    elements, styles, summary_index,
    *,
    preprocessing_results=None,
    dataset_path="",
    selected_model_names=None,
    results_df=None,
    hpo_metric="Q^2",
    best_models_per_target=None,
    uq_before=None,
    uq_after=None,
    uq_settings=None,
    postprocessing_results=None,
    perl_results=None,
    perl_config=None,
    step_timings=None,
    cleaning_summary=None,
    random_seed=None,
):
    """Splice a procedurally generated Executive Summary into `elements` at
    `summary_index` (the slot `init_pdf_report` reserved between the timestamp and the
    Table of Contents), followed by a page break so the Table of Contents always
    starts on its own page regardless of how long the summary is.

    Every figure here is read straight from the workflow's own results: nothing is
    recomputed and nothing is inferred, so the summary can only ever restate what the
    rest of the report already shows. Groups for steps that were not run (or produced
    no data) are simply omitted.
    """
    summary = [Paragraph("Executive Summary", styles["CustomHeading"])]
    summary.append(Paragraph(
        "This executive summary has been procedurally generated using the computed results "
        "of this run. See the corresponding sections later in the report for more detail.",
        styles["CustomBody"],
    ))

    for group in (
        _exec_dataset_group(styles, preprocessing_results, dataset_path, perl_config, cleaning_summary,
                           random_seed=random_seed),
        _exec_modelling_group(styles, selected_model_names, results_df,
                              hpo_metric, best_models_per_target, perl_results,
                              uq_before, uq_after, uq_settings, postprocessing_results,
                              preprocessing_results),
        _exec_diagnostics_group(styles, preprocessing_results, postprocessing_results),
        _exec_computation_group(styles, step_timings),
    ):
        summary.extend(group)

    summary.append(PageBreak())
    elements[summary_index:summary_index] = summary


