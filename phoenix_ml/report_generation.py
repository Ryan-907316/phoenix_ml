# Initialisation of the .pdf document
def init_pdf_report(
    filename="system_report.pdf",
    output_dir=".",
    title="Machine Learning Report",
    font_name="Helvetica",
    font_size=10,
    title_font_size=18,
    heading_font_size=14
):
    # Initialises the .pdf document and returns the doc object, elements list, and styles.
    os.makedirs(output_dir, exist_ok=True)
    filepath = os.path.join(output_dir, filename)

    doc = PhoenixDocTemplate(
        filepath, pagesize=A4, rightMargin=20*mm, leftMargin=20*mm,
        topMargin=20*mm, bottomMargin=22*mm,
        title="phoenix_ml Report", author="phoenix_ml"
    )
    elements = []

    styles = getSampleStyleSheet()
    styles.add(ParagraphStyle(name="CustomTitle", fontName=f"{font_name}-Bold",
                              fontSize=title_font_size, leading=title_font_size + 4, alignment=1))
    styles.add(ParagraphStyle(name="CustomHeading", fontName=f"{font_name}-Bold",
                              fontSize=heading_font_size, spaceAfter=10,
                              leading=heading_font_size + 2, spaceBefore=8))
    styles.add(ParagraphStyle(name="CustomBody", fontName=font_name, fontSize=font_size,
                              spaceAfter=6, leading=font_size + 2))
    styles.add(ParagraphStyle(name="CustomCaption", fontName=f"{font_name}-Oblique",
                              fontSize=font_size - 2, spaceAfter=4, leading=font_size,
                              textColor=colors.HexColor("#555555")))
    styles.add(ParagraphStyle(
        name="CustomSubheading",
        parent=styles["Heading2"],
        fontName="Helvetica-Bold",
        fontSize=12,
        leading=14,
        alignment=TA_LEFT,
        spaceAfter=6,
    ))
    # Title page
    elements.append(Paragraph(title, styles["CustomTitle"]))
    elements.append(Spacer(1, 12))
    elements.append(Paragraph(
        f"This report was generated on: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}",
        styles["CustomBody"],
    ))
    elements.append(Spacer(1, 20))

    # Marks where the Executive Summary is spliced in later, once the workflow's
    # results are known (see add_executive_summary_section): it must appear here,
    # before the Table of Contents, but its content isn't available until the run finishes.
    summary_index = len(elements)

    # Table of Contents
    elements.append(Paragraph(
        "<u><b>Table of Contents</b></u>",
        ParagraphStyle(name="_TOCTitle", fontName="Helvetica-Bold", fontSize=13,
                       leading=16, spaceAfter=10),
    ))
    toc = TableOfContents()
    toc.levelStyles = [
        ParagraphStyle(name="_TOCLevel0", fontName="Helvetica-Bold", fontSize=10,
                       leading=14, leftIndent=0, spaceAfter=2),
        ParagraphStyle(name="_TOCLevel1", fontName="Helvetica", fontSize=9,
                       leading=12, leftIndent=12, spaceAfter=1,
                       textColor=colors.HexColor("#444444")),
    ]
    elements.append(toc)
    elements.append(PageBreak())

    return doc, elements, styles, filepath, summary_index


