def _build_mp_caption(dummy):
    """Return caption paragraphs for the MP-denoised distance correlation figure.

    The dummy-variable paragraph is deliberately NOT repeated here — the original
    distance-correlation figure directly above this one already explains it.
    """
    return [
        "Marchenko-Pastur (MP) thresholding has been applied to denoise the matrix. This technique "
        "uses Random Matrix Theory to estimate how many independent correlation patterns in the data "
        "are stronger than what would arise by chance in a matrix of the same size. Patterns weaker "
        "than this noise floor are discarded, and the matrix is reconstructed from only the remaining "
        "signal patterns. The noise threshold is the MP upper bound (lambda+), and any eigenvalue of "
        "the correlation matrix below this is statistically indistinguishable from random noise at "
        "the given dataset size and is discarded. Features with high values in this denoised matrix "
        "are strongly tied to the retained signal; features near zero after denoising show "
        "correlations no stronger than sampling noise."
    ]


